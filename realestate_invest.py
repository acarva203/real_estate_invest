def main():
    if API_KEY == "YOUR_API_KEY_HERE":
        raise ValueError(
            "Set your Census API key:\n"
            "  export CENSUS_API_KEY=your_key_here\n"
            "Or edit API_KEY at the top of this file.\n"
            "Get a free key at: https://api.census.gov/data/key_signup.html"
        )

    all_years_dfs = []

    for year in YEARS:
        df = fetch_zcta_data(year, zip_prefixes=DEV_ZIP_PREFIXES)
        df = compute_derived_metrics(df)
        all_years_dfs.append(df)
        time.sleep(0.5)  # Be polite to Census servers

    # Save raw long-format data
    df_long = pd.concat(all_years_dfs, ignore_index=True)
    raw_path = os.path.join(OUTPUT_DIR, "acs_raw_long.csv")
    df_long.to_csv(raw_path, index=False)
    print(f"\n✓ Saved raw long-format data → {raw_path}")
    print(f"  Shape: {df_long.shape}")

    # Compute trends (wide format, one row per ZCTA)
    print("\n→ Computing year-over-year trends...")
    df_trends = compute_trends(df_long)

    # Compute opportunity scores
    print("→ Computing opportunity scores...")
    df_scored = compute_opportunity_score(df_trends)

    # Assign investment tiers
    print("\n→ Assigning investment tiers...")
    df_scored = assign_tiers(df_scored)

    # Save scored + tiered output
    scored_path = os.path.join(OUTPUT_DIR, "zcta_scored.csv")
    df_scored.to_csv(scored_path, index=False)
    print(f"\u2713 Saved scored ZCTA data \u2192 {scored_path}")
    print(f"  Shape: {df_scored.shape}")

    # Tier summary
    print("\n" + "=" * 70)
    print("INVESTMENT TIER SUMMARY")
    print("=" * 70)
    for tier in ["BUILD NOW", "WATCH LIST", "EMERGING", "MONITOR"]:
        n = (df_scored["investment_tier"] == tier).sum()
        print(f"  {tier:<12} {n:>4} zip codes")

    for tier in ["BUILD NOW", "WATCH LIST", "EMERGING"]:
        subset = df_scored[df_scored["investment_tier"] == tier].head(10)
        if subset.empty:
            continue
        print(f"\n" + "-" * 70)
        print(f"  {tier}")
        print("-" * 70)
        for _, row in subset.iterrows():
            print(f"  [{row['opportunity_score']:5.1f}]  {row['rationale']}")

    return df_long, df_scored


if __name__ == "__main__":
    df_long, df_scored = main()

