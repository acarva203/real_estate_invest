# ─── FETCH FUNCTIONS ───────────────────────────────────────────────────────────

def build_get_string(variables: dict) -> str:
    """Build the ?get= query string from variable dict."""
    return "NAME," + ",".join(variables.keys())


def fetch_zcta_data(year: int, zip_prefixes: list = None) -> pd.DataFrame:
    """
    Fetch ACS data for ZCTAs in a given year.

    ZCTAs don't nest inside states in Census geography, so we always pull
    the full national ZCTA list and optionally filter by zip prefix afterward.

    Args:
        year: ACS 5-year estimate year (e.g. 2022 = 2018-2022 survey)
        zip_prefixes: list of string prefixes to keep, e.g. ["9"] for CA
                      or ["75","76"] for parts of Texas. None = keep all.
    """
    url = BASE_URL.format(year=year)
    get_str = build_get_string(VARIABLES)

    params = {
        "get": get_str,
        "for": "zip code tabulation area:*",
        "key": API_KEY,
    }

    label = f"[prefixes: {zip_prefixes}]" if zip_prefixes else "[national]"
    print(f"\n→ Fetching {year} ACS data  {label}...")
    resp = requests.get(url, params=params, timeout=120)

    if resp.status_code != 200:
        raise ValueError(
            f"Census API error {resp.status_code} for year {year}:\n{resp.text[:500]}"
        )

    raw = resp.json()
    headers = raw[0]
    rows = raw[1:]

    df = pd.DataFrame(rows, columns=headers)
    df["year"] = year
    df = df.rename(columns={"zip code tabulation area": "zcta"})

    # Filter by zip prefix if requested
    if zip_prefixes:
        mask = df["zcta"].str.startswith(tuple(zip_prefixes))
        df = df[mask].copy()

    # Rename Census variable codes to human-readable names
    df = df.rename(columns=VARIABLES)

    # Keep only relevant columns
    cols_to_keep = ["zcta", "year", "NAME"] + list(VARIABLES.values())
    df = df[[c for c in cols_to_keep if c in df.columns]]

    # Cast numeric columns (Census uses -666666666 as null sentinel)
    for col in VARIABLES.values():
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")
    df = df.replace(-666666666, pd.NA)

    print(f"   ✓ {len(df):,} ZCTAs returned for {year}")
    return df


# ─── DERIVED METRICS ───────────────────────────────────────────────────────────

def compute_derived_metrics(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add computed columns on top of raw Census variables.
    These are the core inputs for the opportunity scoring model.
    """

    # Total population aged 25-44
    age_cols = [
        "male_25_29", "male_30_34", "male_35_39", "male_40_44",
        "female_25_29", "female_30_34", "female_35_39", "female_40_44",
    ]
    df["pop_25_44"] = df[[c for c in age_cols if c in df.columns]].sum(axis=1, skipna=True)

    # % of total population aged 25-44
    df["pct_pop_25_44"] = (df["pop_25_44"] / df["total_population"] * 100).round(2)

    # Total households earning $100k+
    income_100k_cols = [
        "hh_income_100_124k", "hh_income_125_149k", "hh_income_150_174k",
        "hh_income_175_199k", "hh_income_200k_plus",
    ]
    df["hh_income_100k_plus"] = df[[c for c in income_100k_cols if c in df.columns]].sum(axis=1, skipna=True)

    # % of households earning $100k+
    df["pct_hh_income_100k_plus"] = (df["hh_income_100k_plus"] / df["hh_total"] * 100).round(2)

    # Affordability ratio: median income proxy vs home value
    # Lower ratio = more affordability headroom for new construction
    # We use $100k as a rough income proxy since we don't have median income here
    df["home_value_to_income_ratio"] = (df["median_home_value"] / 100000).round(2)

    # Supply-side metrics
    df["pct_owner_occupied"] = (
        df["owner_occupied_units"] / df["total_housing_units"] * 100
    ).round(2)

    # demand_supply_gap: positive = more potential buyers than owner-occupied units
    df["demand_supply_gap"] = (
        df["hh_income_100k_plus"] - df["owner_occupied_units"]
    ).round(0)

    # units_per_target_hh: lower = less supply relative to demand
    df["units_per_target_hh"] = (
        df["total_housing_units"] / df["hh_income_100k_plus"].replace(0, pd.NA)
    ).round(2)

    return df


# ─── CHANGE / TREND CALCULATION ────────────────────────────────────────────────

def compute_trends(df_all_years: pd.DataFrame) -> pd.DataFrame:
    """
    Pivot the multi-year data to compute year-over-year changes
    in key demographic metrics per ZCTA.

    Returns a wide-format DataFrame (one row per ZCTA) with trend columns.
    """
    metrics = [
        "pct_pop_25_44", "pct_hh_income_100k_plus", "total_population",
        "demand_supply_gap", "units_per_target_hh",
    ]
    years = sorted(df_all_years["year"].unique())

    pivot = df_all_years.pivot_table(
        index="zcta",
        columns="year",
        values=metrics,
        aggfunc="first",
    )

    # Flatten column names: e.g. ('pct_pop_25_44', 2019) → 'pct_pop_25_44_2019'
    pivot.columns = [f"{metric}_{year}" for metric, year in pivot.columns]
    pivot = pivot.reset_index()

    # Trend: earliest to most recent year in pull
    year_start = min(years)
    year_end   = max(years)
    suffix     = f"{str(year_start)[2:]}_{str(year_end)[2:]}"

    if year_start in years and year_end in years:
        for metric in metrics:
            col_start = f"{metric}_{year_start}"
            col_end   = f"{metric}_{year_end}"
            if col_start in pivot.columns and col_end in pivot.columns:
                pivot[f"{metric}_change_{suffix}"] = (
                    pivot[col_end] - pivot[col_start]
                ).round(2)
                pivot[f"{metric}_pct_change_{suffix}"] = (
                    (pivot[col_end] - pivot[col_start]) / pivot[col_start] * 100
                ).round(2)

    return pivot


# ─── OPPORTUNITY SCORE ─────────────────────────────────────────────────────────

def compute_opportunity_score(df_wide: pd.DataFrame) -> pd.DataFrame:
    """
    Build a composite opportunity score (0-100) for each ZCTA.

    Scoring dimensions:
        1. Target density (35%)   → pct of high-income 25-44yo households
        2. Growth momentum (25%)  → YoY increase in target demographic
        3. Affordability (25%)    → home value to income headroom
        4. Penetration gap (15%)  → large target pop relative to housing stock
    """

    def normalize(series: pd.Series) -> pd.Series:
        """Min-max normalize a series to 0-100."""
        mn, mx = series.min(), series.max()
        if mx == mn:
            return pd.Series([50.0] * len(series), index=series.index)
        return ((series - mn) / (mx - mn) * 100).round(2)

    df = df_wide.copy()

    def latest_col(prefix):
        """Return most recent column matching prefix, or None."""
        matches = sorted([c for c in df.columns if c.startswith(prefix)], reverse=True)
        return matches[0] if matches else None

    # --- Dimension 1: Target Density ---
    density_col = latest_col("pct_hh_income_100k_plus_2")
    df["score_density"] = normalize(df[density_col].fillna(0)) if density_col else 50.0

    # --- Dimension 2: Growth Momentum ---
    growth_col = latest_col("pct_hh_income_100k_plus_change_")
    df["score_growth"] = normalize(df[growth_col].fillna(0)) if growth_col else 50.0

    # --- Dimension 3: Affordability Headroom ---
    affordability_col = latest_col("home_value_to_income_ratio_2")
    if affordability_col:
        df["score_affordability"] = 100 - normalize(
            df[affordability_col].fillna(df[affordability_col].median())
        )
    else:
        df["score_affordability"] = 50.0

    # --- Dimension 4: Penetration Gap (supply-powered) ---
    # Lower units-per-target-HH = less supply relative to demand = higher opportunity
    supply_col = latest_col("units_per_target_hh")
    if supply_col:
        capped = df[supply_col].clip(upper=df[supply_col].quantile(0.99))
        df["score_penetration"] = 100 - normalize(capped.fillna(capped.median()))
    else:
        fallback = latest_col("pct_pop_25_44_2")
        df["score_penetration"] = normalize(df[fallback].fillna(0)) if fallback else 50.0

    # --- Composite Score ---
    weights = {
        "score_density": 0.35,
        "score_growth": 0.25,
        "score_affordability": 0.25,
        "score_penetration": 0.15,
    }

    df["opportunity_score"] = sum(
        df[col] * weight for col, weight in weights.items()
    ).round(2)

    return df



# ─── TIER LABELS ──────────────────────────────────────────────────────────────────────────

def assign_tiers(df: pd.DataFrame) -> pd.DataFrame:
    """
    Assign each ZCTA to an investment tier with a one-line narrative rationale.

    Tiers (by opportunity_score percentile):
        BUILD NOW   top 15%  -- strong buyer pool + real supply gap today
        WATCH LIST  next 20% -- solid fundamentals with meaningful momentum
        EMERGING    next 25% -- demographic tailwinds, thinner current density
        MONITOR     bottom 40% -- insufficient signal for near-term investment
    """
    df = df.copy()
    df["score_rank_pct"] = df["opportunity_score"].rank(pct=True) * 100

    def _latest(prefix):
        matches = sorted([c for c in df.columns if c.startswith(prefix)], reverse=True)
        return matches[0] if matches else None

    density_col  = _latest("pct_hh_income_100k_plus_2")
    growth_col   = _latest("pct_hh_income_100k_plus_change_")
    supply_col   = _latest("units_per_target_hh")
    hv_col       = _latest("home_value_to_income_ratio_2")

    tiers, rationales = [], []

    for _, row in df.iterrows():
        pct      = row["score_rank_pct"]
        density  = float(row[density_col])  if density_col  and pd.notna(row[density_col])  else 0.0
        growth   = float(row[growth_col])   if growth_col   and pd.notna(row[growth_col])   else 0.0
        supply   = float(row[supply_col])   if supply_col   and pd.notna(row[supply_col])   else 999.0
        hv_ratio = float(row[hv_col])       if hv_col       and pd.notna(row[hv_col])       else 5.0
        zcta     = row["zcta"]

        if pct >= 85:
            tier = "BUILD NOW"
            if density >= 40:
                rationale = (f"ZIP {zcta}: {density:.0f}% high-income households, "
                             f"{supply:.1f} units/target HH -- large pool, tight supply.")
            elif growth >= 10:
                rationale = (f"ZIP {zcta}: target demographic grew {growth:+.1f}pp -- "
                             f"rapid formation + supply gap = first-mover opportunity.")
            else:
                rationale = (f"ZIP {zcta}: top-tier composite across density, momentum, "
                             f"and affordability -- recommend immediate site study.")

        elif pct >= 65:
            tier = "WATCH LIST"
            if growth >= 5:
                rationale = (f"ZIP {zcta}: {growth:+.1f}pp income-demographic growth; "
                             f"monitor supply tightening before land acquisition.")
            elif hv_ratio < 3.5:
                rationale = (f"ZIP {zcta}: home-value/income ratio {hv_ratio:.1f}x leaves "
                             f"pricing headroom -- commit on next positive momentum signal.")
            else:
                rationale = (f"ZIP {zcta}: above-average fundamentals; revisit after "
                             f"next ACS release to confirm sustained trajectory.")

        elif pct >= 40:
            tier = "EMERGING"
            if density < 20 and growth > 0:
                rationale = (f"ZIP {zcta}: modest buyer share ({density:.0f}%) but growing -- "
                             f"early-stage suburban formation, long-horizon play.")
            elif supply < 5:
                rationale = (f"ZIP {zcta}: tight stock ({supply:.1f} units/target HH) signals "
                             f"pent-up demand; buyer density still building.")
            else:
                rationale = (f"ZIP {zcta}: nascent tailwinds -- revisit when income share "
                             f"crosses 25% or supply gap widens further.")

        else:
            tier = "MONITOR"
            rationale = (f"ZIP {zcta}: buyer-pool density and growth insufficient "
                         f"for near-term development investment.")

        tiers.append(tier)
        rationales.append(rationale)

    df["investment_tier"] = tiers
    df["rationale"] = rationales

    order = {"BUILD NOW": 0, "WATCH LIST": 1, "EMERGING": 2, "MONITOR": 3}
    df["_sort"] = df["investment_tier"].map(order)
    df = df.sort_values(["_sort", "opportunity_score"], ascending=[True, False])
    df = df.drop(columns=["_sort", "score_rank_pct"])
    return df


